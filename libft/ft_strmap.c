#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*str;
	int		len;
	int		i;

	if (s && f)
	{
		i = 0;
		len = 0;
		while (s[len] != '\0')
			len++;
		if (!(str = (char *)malloc(sizeof(char) * (len + 1))))
			return (0);
		while (s[i] != '\0')
		{
			str[i] = f(s[i]);
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
