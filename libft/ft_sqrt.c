int	ft_sqrt(int nb)
{
	int res;
	int i;

	i = 0;
	res = 0;
	if (nb < 0)
		return (0);
	while (res != nb)
	{
		i++;
		res = i * i;
		if (i == 50000)
			return (0);
	}
	return (i);
}
